  return new Intl.DateTimeFormat('zh-CN', {
    year: 'numeric',
    month: 'short',
    day: 'numeric'
  }).format(date)
}

// Êà™Êñ≠ÊñáÊú¨
const truncateText = (text: string, maxLength: number) => {
  if (!text) return ''
